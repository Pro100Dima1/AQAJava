package org.expressJava.complexTask.validator2;

public class User {
    private String name;
    private String email;
    private int age;

    public User(String name, String email, int age) {
        this.name = name;
        this.email = email;
        this.age = age;
    }

    public String getName2() {
        return name;
    }

    public String getEmail2() {
        return email;
    }

    public int getAge2() {
        return age;
    }
}
