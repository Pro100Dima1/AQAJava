package org.cleanCode.task1.dry;

public class MathOperations {
    public int summations(int a, int b) {
        return a + b;
    }

    public int summations(int a, int b, int c) {
        return a + b + c;
    }

    public int summations(int a, int b, int c, int d) {
        return a + b + c + d;
    }
}
